	<p>
		I had the privilege to travel Flatland together with A. Square these past few days and the experience was truly delightful.
	</p>
	<p>
		The first half of the book is dedicated to the exploration of flatland society, customs, and explaining how life works in general. This section is mainly meant as critique of the Victorian society, so one could expect it to be irrelevant and boring. But it is nothing of the sort! Even though a lot of the politics is no longer recent, it is still a fun and interesting journey through flatland. So even though I was at first tempted to skip reading it and jump straight into the other dimensions, I am now very glad I didn’t and I would advise others not to do it either.
	</p>
	<p>
		The second half is really where the dimensional shenenigans pick off. First, the protagonist (I’ll call him Albert* from now on) visits Lineland in a dream and converses with its king. This visit explores how life might work in a one dimensional world and Albert tries to explain to the Line King how he might be able to visit, or at least comprehend the second dimension. The King is unable to do so, to the frustration of Albert.
	</p>
	<p>
		Then Albert is visited by a sphere who tries to explain to him, in a manner very similar to that which Albert used on the King only a short while ago, how Albert might be able to perceive the third dimension and, to the frustration of both, fails in doing so.
	</p>
	<p>
		Somehow, the sphere manages to take Albert out of the Flatland plane and somehow his eye develops the ability to see in two dimensions. In my opinion, the eye and brain completely changing their function once taken out of the Flatland plane (as opposed to funcioning in the same way, just on a different plane) is the most immersion breaking and unbelievable part of the book, but I do understand that this was the easiest way to keep the story moving in its “climax”.
	</p>
	<p>
		After that, Albert and the sphere observe Flatland from the third dimension and there are some hints of exploring the fourth dimension and even beyond, but unfortunately this topic is not explored as much as I’d like.
	</p>
	<p>
		Then the book finishes with more political exploration in which I, to be completely honest, was not too interested. The point dimension is also visited, but I would include it in the political babble.
	</p>
	<p>
		Over all I really enjoyed reading Flatland and I’d recommend anyone interested in exploring mainly the second and maybe other dimensions to do so as well. The exploration of how life might work on a two-dimensional plane was really interesting and I enjoyed it thoroughly. And the hints of how one might be able to envision the axis, or at least the movement along the axis new to the dimension one-higher than he is on, and what he might see if he were granted vision capable of seeing one dimension higher than what he already sees, were really useful.
	</p>
	<p>
		One tool you might find useful for exploring the fourth dimension in an interactive way might be [4D miner](https://4d-miner.fandom.com/wiki/4D_Miner), but I have not tried playing it yet, so I cannot recommend it directly.
	</p>
	<hr>
	<p>
		*The name Albert is never actually given to him in the book, but a sequel by a different author, Flatterland, asserts this name, so I will use it.
</p>
